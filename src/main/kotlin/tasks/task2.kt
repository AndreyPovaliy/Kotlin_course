package tasks

/*Для двух целых чисел вывести на экран «первое число больше»
если первое больше, «второе число больше»
если второе больше и вывести «числа равны», если они равны*/

fun main(){
    val number1: Int = readln().toInt()
    val number2: Int = readln().toInt()

    if (number1 > number2)
        println("первое число больше")
    else
        println("первое число больше")

}